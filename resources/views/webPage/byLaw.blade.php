<p class=MsoListParagraphCxSpMiddle style='margin-left:14.2pt;mso-add-space:
auto;text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>22.</span></span><![endif]>Habrá puestos de hidratación
en el ciclismo, puestos de hidratación y abastecimiento en el pedestrismo.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:14.2pt;mso-add-space:
auto;text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>23.</span></span><![endif]>El uso del chip de
clasificación es obligatorio durante toda la prueba.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:14.2pt;mso-add-space:
auto;text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
