style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>28.</span></span><![endif]>La organización tiene el
poder de tomar cualquier decisión en relación a las reglas de la competencia.</p>

<p class=MsoListParagraphCxSpLast style='margin-left:14.2pt;mso-add-space:auto;
text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>29.</span></span><![endif]>Cualquier duda sobre este
reglamento deberá ser consultada en la charla técnica el día anterior a la
competencia.</p>

<p class=MsoNormal style='line-height:normal'><b style='mso-bidi-font-weight:
normal'>Natación (1.9 Km)<o:p></o:p></b></p>

<p class=MsoListParagraphCxSpFirst style='margin-left:14.2pt;mso-add-space:
auto;text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>30.</span></span><![endif]>El atleta deberá respetar de
forma correcta el recorrido establecido por la organización. Quien no cumpla
con esta regla será descalificado de la carrera.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:14.2pt;mso-add-space:
auto;text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>31.</span></span><![endif]>Los atletas están obligados
a usar el gorro provisto por la organización. El atleta que no cumpla con esta
regla será descalificado de la carrera.</p>

<p class=MsoListParagraphCxSpLast style='margin-left:14.2pt;mso-add-space:auto;
text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
