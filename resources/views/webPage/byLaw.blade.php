style='mso-list:Ignore'>32.</span></span><![endif]>Está terminantemente
prohibido usar cualquier equipamiento auxiliar para nadar. El atleta que no
cumpla con esta regla será descalificado de la carrera, salvo el uso del traje
de neoprene que si estará permitido.</p>

<p class=MsoNormal style='line-height:normal'><b style='mso-bidi-font-weight:
normal'>Ciclismo (90 km.)<o:p></o:p></b></p>

<p class=MsoListParagraphCxSpFirst style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>33.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>El atleta no podrá tocar el equipamiento de los
demás competidores dentro del parque cerrado.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>34.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>El atleta deberá tener el casco debidamente
colocado antes de agarrar su bicicleta.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>35.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>El atleta no podrá subirse a su bicicleta hasta
haber pasado por la salida del área de transición. El competidor que no cumpla
con esta regla será penalizado.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>36.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>Es obligatorio para todos los atletas tener el
torso cubierto. Quien no cumpla con esta regla será descalificado de la
competencia.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>37.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>El número de competidor deberá estar visible en
la bicicleta (de la manera en que lo indique la organización), en la espalda
del atleta y en la parte frontal del casco. Tanto los calcos como el dorsal no
podrán ser alterados de su forma original. Quien no cumpla con esta regla será
descalificado de la competencia. </p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>38.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>Está prohibido el <span class=SpellE>drafting</span>
(rodar en pelotón), el atleta deberá mantener una distancia mínima de 10 metros
con el atleta de adelante. El atleta que no cumpla con esta regla será penalizado.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>39.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]><span style='mso-spacerun:yes'> </span>El atleta
tendrá 20 segundos para sobrepasar a otro atleta. El atleta que no cumpla con esta
regla será <span class=GramE>penalizada</span>.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>40.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]><span style='mso-spacerun:yes'> </span>Se
considera que un atleta sobrepaso a otro, cuando su rueda delantera sobrepasa
la del otro competidor. El atleta sobrepasado, no podrá reaccionar al ataque y
tendrá 20 segundos para salir de la zona del otro competidor. Una vez que esté
a 10 metros de distancia podrá comenzar una nueva acción de sobrepaso. El
atleta que no cumpla con esta regla será penalizado.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>41.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>Está prohibido el uso de auriculares; el uso de
los mismos será penalizado.</p>

<p class=MsoListParagraphCxSpLast style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-17.85pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>42.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>Si bien habrá controles de vueltas, cada atleta
es responsable de contar sus giros</p>

<p class=MsoNormal style='line-height:normal'><b style='mso-bidi-font-weight:
normal'>Carrera (21km)<o:p></o:p></b></p>

<p class=MsoListParagraphCxSpFirst style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-18.0pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>43.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>El número de competidor deberá estar ubicado en
la parte delantera del cuerpo del atleta, de manera visible y sin alteraciones.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-18.0pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>44.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>Los atletas no podrán ser guiados o acompañados
por personas ajenas a la competencia quien no cumpla con esta regla, será
penalizado.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;line-height:150%'>En caso de reiterarse dicho
incumplimiento, el atleta será descalificado de la prueba.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-18.0pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>45.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>Está prohibido el uso de auriculares; el uso de
los mismos será penalizado.</p>

<p class=MsoListParagraphCxSpLast style='margin-left:17.85pt;mso-add-space:
auto;text-align:justify;text-indent:-18.0pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
style='mso-list:Ignore'>46.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;
</span></span></span><![endif]>Todos los atletas deberán tener el torso
cubierto. Quien no cumpla con esta regla será descalificado de la competencia.</p>

<p class=MsoNormal style='text-align:justify;line-height:150%'><b
style='mso-bidi-font-weight:normal'><span style='color:red'>IMPORTANTE: En caso
de que debido a condiciones meteorológicas adversas y bajo la orden de las
autoridades responsables, no se pueda efectuar la disciplina de natación, la
prueba se transformará en un DUATLON, con las distancias de 5km. Carrera – 90
km. De ciclismo – 21 km. de carrera. La organización podrá tomar la
determinación de suspender la prueba o acortar las distancias por razones de
fuerza mayor. En caso de suspensión de la prueba se reprogramará la fecha, no
pudiendo el atleta reclamar la devolución del importe abonado en concepto de
inscripción.<o:p></o:p></span></b></p>

