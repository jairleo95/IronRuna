evento recibirá una medalla. Los 3 primeros atletas de cada categoría recibirán
un trofeo, medalla. <span style='mso-spacerun:yes'> </span></p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:14.2pt;mso-add-space:
