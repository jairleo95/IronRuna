competencia podrá retirar de la prueba a cualquier atleta si de acuerdo a su
criterio médico se pone en riesgo la integridad física de la persona.</p>

<p class=MsoListParagraphCxSpMiddle style='margin-left:14.2pt;mso-add-space:
auto;text-align:justify;text-indent:-14.2pt;line-height:150%;mso-list:l2 level1 lfo1'><![if !supportLists]><span
style='mso-bidi-font-family:Calibri;mso-bidi-theme-font:minor-latin'><span
